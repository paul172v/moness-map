import L from "leaflet";
export class Cottage {
  number: number;
  name: string;
  lat: number;
  lng: number;
  isActive: boolean;
  icon: L.Icon;

  constructor(number: number, name: string, lat: number, lng: number) {
    this.number = number;
    this.name = name;
    this.lat = lat;
    this.lng = lng;
    this.isActive = false;
    this.icon = L.icon({
      iconUrl: "/cabin.svg",
      iconSize: [30, 30],
      iconAnchor: [15, 15],
    });
  }
}

export class Misc {
  name: string;
  lat: number;
  lng: number;
  icon: L.Icon;

  constructor(
    name: string,
    lat: number,
    lng: number,
    iconUrl: string,
    iconSize: [number, number]
  ) {
    this.name = name;
    this.lat = lat;
    this.lng = lng;
    this.icon = L.icon({
      iconUrl: iconUrl,
      iconSize: iconSize,
      iconAnchor: [15, 15],
    });
  }
}

